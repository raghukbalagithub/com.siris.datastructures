package com.siris.datastructures.linkedlist;

public class SingleNode {
    int value;
    SingleNode next;

    public SingleNode getNext() {
        return next;
    }

    public void setNext(SingleNode next) {
        this.next = next;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
